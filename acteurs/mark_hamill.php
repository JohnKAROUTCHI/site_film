      <section id="bio">
        <h2>BIOGRAPHIE</h2>
        <p>
          Fils d'un capitaine de la US Navy, Mark Richard Hamill, de son nom complet,
          grandit en Californie, en Virginie, à New York puis au Japon, avec ses six
          autres frères et soeurs. Dans les différents lycées qu'il fréquente, il
          s'essaie au théâtre dans les clubs et associations scolaires. Il obtient
          finalement un diplôme d'art dramatique au Los Angeles City College.<br>

          Il parvient d'abord à jouer de petits rôles secondaires dans diverses séries
          et émissions, notamment le "Bill Cosby Show", en 1969, avant de s'intéresser
          au doublage pour dessins animés. Sa première incarnation récurrente se fait
          ainsi dans la série animée "Jeannie", d'Hanna Barbera, de 1973 à 1975.
          Continuant à apparaître sur le petit écran, on le retrouve dans l'un des
          principaux rôles de "Texas Wheelers" en 1974-75. Il faut attendre 1977 pour
          le voir prendre part à un projet  cinématographique, une voix dans le film
          d'animation Les Sorciers de la Guerre.<br>

          C'est grâce à son ami Robert Englund qu'il passe une audition pour jouer
          Luke Skywalker dans La Guerre des étoiles. Le personnage marque sa
          consécration puisqu'il l'incarne à trois reprises pour George Lucas et sa
          trilogie Star Wars. L'Empire contre-attaque (1980) et Le Retour du Jedi (1983)
          lui valent chacun un Saturn Award du Meilleur acteur. Il joue par la suite
          dans plusieurs adaptations radio de la saga.<br>

          En 1977, il est victime d'un grave accident de voiture lui brisant le nez
          et la machoire. 7 heures d'opération chirurgicale seront nécessaires pour le
          remettre sur pied. Il épouse Marilou York en 1978, dont il aura trois enfants.
          Disparaissant majoritairement des écrans durant les années 80, il se spécialise
          dans le théâtre et le doublage de personnages animés. Il est depuis l'un des
          doubleurs les plus recherchés aux USA, notamment pour son incarnation du Joker
          dans les jeux vidéo et séries Batman et dans les diverses séries DC comics et
          Marvel (comme les séries des années 90 Spider-man, Superman et Hulk). Il a
          par ailleurs doublé des personnages de Miyazaki, dans Nausicaa la vallée du
          vent et Le Château dans le ciel. Le Joker anglophone animé utilisa sa voix de
          1993 à 2012, parfois dans des univers très décalés comme Robot Chicken.<br>

          Les années 90 le ramènent à l'écran, dans des séries B plus ou moins réussies
          mais il préfère continuer à ne jouer que de sa voix pour des dessins animés
          cultes comme Casper ou Hey Arnold. Durant les années 2000, outre son
          incarnation récurrente du Joker sur différents supports, on peut entendre sa
          voix dans plusieurs épisodes des Griffin, de Seth McFarlane, ainsi que dans
          les séries animées à succès La Ligue des justiciers, Avatar le dernier maître
          de l'air, Adventure Time et Dragons, cavaliers de Beurk.<br>

          En 2014, il revient pour une quatrième apparition dans Star Wars VII avec
          plusieurs acteurs originaux du film de 1977. Le très attendu film sortira fin
          2015.
        </p>
      </section>

      <section id="bio">
        <h2>BIOGRAPHIE</h2>
        <p>
          Tout en travaillant dans le secteur publicitaire, il tente d'entrer à la
          Royal Academy of Dramatic Art mais ne peut obtenir une bourse. Il suit
          des études d'art dramatique pendant quelques mois au Fay Compton Studio
          of Dramatic Art. Il rencontre à cette époque John Gielgud, qui tente de
          l'aider à percer dans le métier d'acteur, mais il débutera vraiment sa
          carrière sur scène dans le célèbre théâtre londonien Old Vic, d'abord par
          de petits rôles.<br>

          En 1941, il s'engage dans la Royal Navy.<br>

          Alors qu'il n'avait joué qu'un figurant dans Prima Donna en 1934, il
          revient au cinéma en 1946 dans Les Grandes Espérances, de David Lean.
          Il devient une figure emblématique des Studios Ealing, notamment par ses
          prestations dans les comédies comme Noblesse oblige (Kind Hearts and
          Coronets) en 1949 (où il interprète huit rôles différents dont celui
          d'une femme) ou Tueurs de dames (The Ladykillers), en 1955 (pour lequel
          il a confié s'être inspiré du jeu d'Alastair Sim pour composer son
          personnage, et où l'on trouve au nombre de ses partenaires, un certain
          Peter Sellers).<br>

          Il est aussi un familier des films de David Lean puisqu'on le retrouve,
          entre autres, dans Oliver Twist (1948), Lawrence d'Arabie (1962), Docteur
          Jivago (1965) et surtout Le Pont de la rivière Kwaï (1957) où son
          interprétation du colonel Nicholson lui apporte un oscar et un Golden
          Globe.<br>

          Il tient également le rôle d'Hitler en 1973 dans Les Dix Derniers Jours
          d'Hitler (Hitler: The Last Ten Days).<br>

          Une autre génération de spectateurs le connaît également pour son rôle
          d'Obi-Wan Kenobi dans Star Wars, un rôle qu'il avait ouvertement détesté.<br>

          Après soixante ans de carrière, Alec Guinness a assuré la relève en
          formant son fils Matthew, qu'on a vu dans La promise ou Lady Jane, et
          qui tourne toujours, bien que, ces dernières années, il n'ait joué que
          dans de simples productions télévisuelles comme Eskimo Day, réalisé en
          1996. Il avait épousé Merula Salaman en 1938.<br>
        </p>
      </section>

      <section id="bio">
        <h2>BIOGRAPHIE</h2>
        <p>
          Carrie Fisher fait ses premiers pas d'actrice au théâtre aux côtés de sa
          mère Debbie Reynolds. En 1975, elle décroche son premier rôle sur grand
          écran, dans la comédie romantique Shampoo d'Hal Ashby, écrite et
          interprétée par Warren Beatty.<br>

          Deux ans plus tard, elle crève littéralement l'écran avec tous ses
          partenaires de Star wars de George Lucas. En y incarnant Leia Organa,
          sénatrice impériale qui se rallie à l'Alliance en lutte contre le
          terrible Empire galactique. Un rôle qu'elle reprend pour L'Empire
          contre-attaque d'Irvin Kershner en 1980 et Le Retour du Jedi de Richard
          Marquand. Entre temps, la popularité immédiate de la comédienne lui
          permet d'assurer une apparition déjantée dans The Blues brothers de John
          John Landis (1980).<br>

          Contre coup du succès ou volonté de s'éloigner du milieu, Carrie Fisher
          se fait plus rare sur les écrans dès la fin de la trilogie imaginée par
          George Lucas. On la voit tout de même dans Hannah et ses soeurs de Woody
          Allen (1986), en confidente dans Quand Harry rencontre Sally de Rob
          Reiner (1989) ou encore dans une apparition éclaire et non créditée dans
          Hook ou la revanche du Capitaine Crochet de Steven Spielberg.<br>

          Caméo clin d'oeil dans Austin Powers (1997), Scream 3 (2000), Beautés
          empoisonnées (2001) et Jay & Bob contre-attaquent (2002), Carrie Fisher
          incarne en 2003 la mère supérieure dans Charlie's Angels: Les anges se
          déchaînent. Mais ne composant pas avec de grands rôles, elle joue davantage
          dans des séries TV comme Smallville, Weeds ou encore Entourage. Depuis 2005,
          elle prête également sa voix au personnage d'Angela dans la série animée Les
          Griffin.<br>

          S'amusant de sa renommée, elle joue son propre personnage le temps d'un
          épisode dans la série Sex & the City et fait aussi preuve d'autodérision dans
          le choix de ses rôles avec la série The Big Bang Theory et le long-métrage de
          David Cronenberg Maps To The Stars.<br>

          Elle reprend en 2015 son rôle de Leia dans Le Réveil de la Force et avait
          achevé peu avant sa mort le tournage de Star Wars Episode VIII.
        </p>
      </section>

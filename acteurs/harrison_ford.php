      <section id="bio">
        <h2>BIOGRAPHIE</h2>
        <p>
          C'est sur les planches de son lycée qu'Harrison Ford débute sa carrière
          artistique. Faisant ses premiers pas au cinéma dans Un truand (1963), La
          Poursuite des tuniques bleues (1967) et Zabriskie Point (1970), il joue
          quelques rôles secondaires sous la direction de Francis Ford Coppola
          (Conversation secrète et Apocalypse Now) et s'illustre dans deux films
          dont l'action se déroule durant la Seconde Guerre mondiale : L'Ouragan
          vient de Navarone (1978) et Guerre et passion (1979). Georges Lucas avait
          repéré le bonhomme et lui avait donné un rôle dans American graffiti en
          1973. Mais c'est grâce à deux sagas au succès planétaire, Star Wars et
          Indiana Jones, que le comédien accède au rang de super star dans les
          années 80.<br>

          Tournant le plus souvent sous la direction de cinéastes confirmés comme
          Ridley Scott (Blade Runner), Peter Weir (Witness, Mosquito Coast), Roman
          Polanski (Frantic), Mike Nichols (Working Girl, A propos d'Henry) et Alan
          J. Pakula (Présumé innocent), Harrison Ford aime à jouer sur la
          dichotomie action hero / père de famille tranquille. C'est ainsi qu'au
          début des années 90, il incarne avec brio l'analyste de la CIA Jack Ryan
          dans Jeux de guerre et Danger immédiat, deux films réalisés par Phillip
          Noyce. Eloigné des rôles physiques (Le Fugitif, Ennemis rapprochés ou Air
          Force One), l'acteur ne parvient guère à convaincre comme le prouvent ses
          échecs au box-office avec Six jours sept nuits (1997) et L'Ombre d'un
          soupçon (1999).<br>


          Dans les années 2000, le comédien se fait plus rare à l'écran, s'essayant
          à des rôles de contre-emploi dans Apparences (2000) et K-19 : le piège
          des profondeurs (2002) lorsqu'il ne s'illustre pas dans des thrillers de
          facture plus classique et peu lucratifs comme Hollywood Homicide (2003)
          et Firewall (2006). Il faut attendre 2008 pour retrouver Harrison Ford,
          alors âgé de 66 ans, dans le quatrième opus des aventures du célèbre
          archéologue risque-tout, Indiana Jones et le Royaume du Crâne de Cristal.<br>

          Dans la foulée, il enchaîne en 2010 avec le tournage de deux films au
          sujet plus grave : Mesures exceptionnelles, l'histoire vraie d'un
          chercheur sur le point de trouver un remède à une maladie génétique rare,
          et Droit de passage, un drame sur l'immigration clandestine aux Etats-Unis,
          dans lequel il donne la réplique à Ray Liotta et Ashley Judd. L'année
          suivante, il incarne un journaliste aigri dans une comédie intitulée
          Morning Glory aux côtés de Diane Keaton, et un cowboy vieillissant dans
          le western de science-fiction Cowboys & envahisseurs. Il s'essaye ensuite
          au genre dystopique dans La Stratégie Ender sous les traits du colonel Hyrum
          Graff, au côté des étoiles montantes Asa Butterfield et Abigail Breslin.
          Mais l'acteur peine à renouer avec le succès.<br>

          A 70 ans passés, Harrison Ford n'est cependant pas près de disparaître
          des écrans. Après avoir rejoint le casting musclé du troisième volet de
          la saga Expendables (2014), il retrouve le costume de Han Solo pour les
          besoins du prochain épisode de Star Wars qui, avec plus de deux milliards
          de recettes mondiales, se hisse à la trosième place du classement des
          plus gros succès au box-office.
        </p>
